package learn_string;

public class LearnIf {
    public static void main(String[] args) {
        int number =0;
        if (number >0){
            System.out.println("is a positive number");
        }
        else if(number <0){
            System.out.println("is a negative number");
        }else {
            System.out.println("not positive or negative");
        }
    }
}
